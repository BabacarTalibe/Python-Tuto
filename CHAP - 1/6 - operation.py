#Arithmetic
a = 10
b = 5
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)

#Assignment
x = 10
x += 5  # x = x + 5
print("Assignment:", x)
#Comparison
y = 20
print("Equal:", x == y) # x is not equal to y
print("Not Equal:", x != y)
print("Greater than:", x > y)
print("Less than:", x < y)
print("Greater than or equal to:", x >= y)
print("Less than or equal to:", x <= y)

#Uniary Minus
z = -x  # negation
print("Unary Minus:", z)

#Logical
is_true = True
is_false = False
print("Logical AND:", is_true and is_false)
print("Logical OR:", is_true or is_false)
print("Logical NOT:", not is_true)

#boolean
is_valid = True
not_valid = False
print("Boolean value:", is_valid)
print("Boolean value:", not_valid)  
print("Boolean value:", is_valid and not_valid)